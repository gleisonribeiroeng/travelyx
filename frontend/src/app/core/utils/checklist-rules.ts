/**
 * Simple heuristic: returns true if the destination likely is NOT a
 * Brazilian city/destination.
 */
export function isInternational(destination: string): boolean {
  const lower = destination.toLowerCase().trim();
  return !BRAZILIAN_CITIES.some((city) => lower.includes(city));
}

