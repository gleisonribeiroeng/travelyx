/**
 * Generate a merged checklist by running all applicable rules against the
 * trip.  Items already present in `existingItems` (matched by ruleId + label)
 * are kept as-is; only genuinely new items are appended.
 */
export function generateChecklist(
  trip: Trip,
  existingItems: ChecklistItem[],
): ChecklistItem[] {
  const existingKeys = new Set(
    existingItems.map((item) => `${item.ruleId}::${item.label}`),
  );

  const newItems: ChecklistItem[] = [];

  for (const rule of RULES) {
    if (!rule.condition(trip)) continue;

    const generated = rule.generate(trip);

    for (const partial of generated) {
      const key = `${partial.ruleId}::${partial.label}`;
      if (existingKeys.has(key)) continue;

      newItems.push({
        id: crypto.randomUUID(),
        isChecked: false,
        autoGenerated: true,
        ...partial,
      });
      existingKeys.add(key); // prevent duplicates within the same generation pass
    }
  }

  return [...existingItems, ...newItems];
}
